// W1 §7 canonicalization: strip a leading UTF-8 BOM, normalize CRLF -> LF,
// nothing else. Applied everywhere bytes are parsed, hashed, or compared.
export function canonicalize(text) {
  return String(text).replace(/^\uFEFF/, "").replace(/\r\n/g, "\n");
}

export function sha256(text) {
  return `sha256:${createHash("sha256").update(text, "utf8").digest("hex")}`;
}

function isSafetyBlock(block) {
  return block.tags.some((tag) => SAFETY_TAGS.has(tag));
}

function renderBlock(block) {
  // W1 §4 block rendering, identical across targets: heading title with tags
  // stripped, HTML provenance comment naming the true source file, body
  // verbatim. Source-level ## blocks render as ### so they nest under the
  // generated ## sections.
  const heading = `### ${block.title} <!-- (from .agentctx/${block.file}) -->`;
  return block.body ? `${heading}\n${block.body}` : heading;
}

// Canonical source-block text a block-manifest entry digests: the parsed
// block's raw heading and trimmed body, exactly the fields parseMarkdownBlocks
// produced from already-canonicalized source bytes. Deterministic by
// construction (no time, randomness, absolute path, or locale input), so the
// digest is a pure function of the source block's content — the same
// determinism contract the artifact bytes obey (W1 §7).
function blockSourceCanonical(block) {
  if (!block.heading) return block.body;
  return block.body ? `${block.heading}\n${block.body}` : block.heading;
}

/**
 * One block-manifest entry (Phase 1 provenance core): the traceability record
 * for a single emitted block. `source_file` is the block's TRUE origin file
 * (preserved across the safety sweep), `source_block_index` its position
 * within that source file, `source_block_digest` a content fingerprint of the
 * source block, `rendered_digest` the fingerprint of the emitted text,
 * `emitted_index` its position in the emitted block sequence, `section` the
 * generated section it renders under, and `forced` whether the safety sweep
 * pulled it into Constraints from an excluded file.
 *
 * Pure: derived from the same `renderBlock` primitive and the parsed block,
 * with no I/O. The manifest is computed alongside the artifact but never mixed
 * into the artifact bytes (backward-compat / determinism).
 */
function buildBlockManifestEntry(block, renderedText, { emittedIndex, section, forced }) {
  return {
    source_file: block.file,
    source_block_index: block.index,
    source_block_digest: sha256(blockSourceCanonical(block)),
    rendered_digest: sha256(renderedText),
    emitted_index: emittedIndex,
    section,
    forced,
  };
}

/**
 * Build one target artifact entirely in memory.
 * Returns { target, path, profile, payload, artifact, sourceDigest,
 *           contentDigest, payloadLines, lineContributions, blockManifest }.
 * `blockManifest` is an in-memory provenance array (one entry per emitted
 * block); it is NOT serialized into `artifact`/`payload` and does not affect
 * the emitted bytes.
 */
export function buildArtifact({ sources, target, profile = "default" }) {
  const spec = EMIT_TARGETS[target];
  if (!spec || !spec.v1) {
    throw new Error(
      `--target must be one of ${V1_TARGET_IDS.map((id) => `"${id}"`).join(", ")}, got: ${target}`,
    );
  }
  const rules = PROFILES[profile];
  if (!rules) {
    throw new Error(`Unknown emit profile: ${profile}`);
  }

  const canonical = {};
  const blocksByFile = {};
  for (const file of SOURCE_FILES) {
    canonical[file] = canonicalize(sources[file] ?? "");
    blocksByFile[file] = parseMarkdownBlocks(canonical[file], file);
  }

  // Safety sweep (W1 principle 4): safety-tagged blocks in excluded files are
  // forced into the Constraints section. A block selected by its own file's
  // rule is never re-appended. cq.md is exempt (SWEEP_EXEMPT_FILES).
  const forcedBlocks = [];
  const forcedFiles = new Set();
  for (const file of SOURCE_FILES) {
    if (rules[file] !== "none" || SWEEP_EXEMPT_FILES.has(file)) continue;
    for (const block of blocksByFile[file]) {
      if (isSafetyBlock(block)) {
        forcedBlocks.push(block);
        forcedFiles.add(file);
      }
    }
  }

  // Sections in SOURCE_FILES order; a section with zero blocks is omitted
  // entirely, heading included (W1 §4 empty-section rule). The block manifest
  // is accumulated in lock-step with the rendered blocks so emitted_index and
  // ordering are guaranteed consistent with the artifact bytes; it never feeds
  // back into `sections`/`payload`.
  const sections = [];
  const blockManifest = [];
  const lineContributions = new Map();
  const countLines = (file, text) => {
    lineContributions.set(
      file,
      (lineContributions.get(file) ?? 0) + text.split("\n").length,
    );
  };
  for (const file of SOURCE_FILES) {
    if (rules[file] === "none") continue;
    const nativeCount = blocksByFile[file].length;
    const blocks = [...blocksByFile[file]];
    if (file === "constraints.md") blocks.push(...forcedBlocks);
    if (blocks.length === 0) continue;
    const rendered = blocks.map((block, i) => {
      const text = renderBlock(block);
      countLines(block.file, text);
      // A constraints.md slot past the native blocks is a safety-forced block
      // swept in from an excluded file (W1 principle 4).
      const forced = file === "constraints.md" && i >= nativeCount;
      blockManifest.push(
        buildBlockManifestEntry(block, text, {
          emittedIndex: blockManifest.length,
          section: SECTION_TITLES[file],
          forced,
        }),
      );
      return text;
    });
    sections.push(`## ${SECTION_TITLES[file]}\n\n${rendered.join("\n\n")}`);
  }

  const frame = FRAMES[target];
  const payload =
    [spec.title, frame.notice.join("\n"), ...sections, frame.footer.join("\n")].join(
      "\n\n",
    ) + "\n";

  // source_digest (W1 §6): fingerprint of the inputs over every
  // profile-included file, in SOURCE_FILES order, plus the one asymmetry:
  // excluded files that currently contribute >=1 safety-forced block are
  // digested too, so edits to them re-flag the artifact.
  const digestParts = [];
  for (const file of SOURCE_FILES) {
    if (rules[file] === "none" && !forcedFiles.has(file)) continue;
    digestParts.push(`${file}\n${canonical[file]}\0`);
  }
  const sourceDigest = sha256(digestParts.join(""));
  const contentDigest = sha256(payload);

  const header = [
    HEADER_OPEN,
    `target: ${target}`,
    `profile: ${profile}`,
    `emit_version: ${EMIT_VERSION}`,
    "source: .agentctx/",
    `source_digest: ${sourceDigest}`,
    `content_digest: ${contentDigest}`,
    `note: ${HEADER_NOTE}`,
    HEADER_CLOSE,
  ].join("\n");

  const payloadLines = payload.split("\n").length - 1; // newline-terminated lines

  return {
    target,
    path: spec.path,
    profile,
    payload,
    artifact: `${header}\n${payload}`,
    sourceDigest,
    contentDigest,
    payloadLines,
    lineContributions,
    blockManifest,
  };
}

/**
 * Parse a generated-file header (W1 §6) from canonicalized artifact content.
 * Returns { header, payload } or null when the content is not a managed
 * artifact (no header block, unparseable line, or missing a required key).
 * Unknown keys are ignored (forward compatibility).
 */
export function parseEmitHeader(content) {
  const lines = String(content).split("\n");
  if (lines[0] !== HEADER_OPEN) return null;
  const header = {};
  let closeIndex = -1;
  for (let i = 1; i < lines.length; i += 1) {
    if (lines[i] === HEADER_CLOSE) {
      closeIndex = i;
      break;
    }
    const m = lines[i].match(/^([A-Za-z0-9_-]+): (.*)$/);
    if (!m) return null;
    header[m[1]] = m[2];
  }
  if (closeIndex === -1) return null;
  for (const key of REQUIRED_HEADER_KEYS) {
    if (!(key in header)) return null;
  }
  return { header, payload: lines.slice(closeIndex + 1).join("\n") };
}

// Operator-guidance detail sentences (W1 §8 per-class table; wording locked
// here, mirrored in docs/cli-errors.md, and asserted by the check matrix
// guard test).
export function detailFor(status, target, path) {
  switch (status) {
    case "missing":
      return `artifact has never been emitted (or was deleted); run: mind-ontology emit --target ${target}`;
    case "unmanaged":
      return `file exists but is not managed by emit; emit will not touch it without --force. Move hand-written content into .agentctx/ sources, then run: mind-ontology emit --force --target ${target}`;
    case "hand-edited":
      return `file was edited after generation; hand edits will be lost on re-emit. Port the edits into .agentctx/ sources, then re-emit. Diff hint: git diff ${path}`;
    case "stale":
      return `.agentctx/ changed (or emit_version bumped) since last emit; run: mind-ontology emit --target ${target}`;
    default:
      return null;
  }
}

/**
 * Classify one target (W1 §8): mutually exclusive classes, checked in order
 * MISSING -> UNMANAGED -> HAND-EDITED -> STALE -> OK. The hand-edit check
 * runs before staleness so the artifact disagreeing with itself is reported
 * independently of whether sources also moved.
 */
export function classifyTarget({ cwd, target, sources }) {
  const spec = EMIT_TARGETS[target];
  const path = resolve(cwd, spec.path);

  if (!existsSync(path)) {
    return {
      target,
      path: spec.path,
      status: "missing",
      detail: detailFor("missing", target, spec.path),
    };
  }

  const content = canonicalize(readFileSync(path, "utf8"));
  const parsed = parseEmitHeader(content);
  if (!parsed) {
    return {
      target,
      path: spec.path,
      status: "unmanaged",
      detail: detailFor("unmanaged", target, spec.path),
    };
  }

  // Carry the RECORDED header fields (target + profile, as written on disk) on
  // every header-present result. These can diverge from the requested `target`
  // / current generator (e.g. a header recording `target: cursor`), so both
  // explain and reconcile must judge reproducibility from these, not from the
  // requested target — single source of truth, no fork.
  const recorded = {
    recordedTarget: parsed.header.target,
    recordedProfile: parsed.header.profile,
  };

  if (sha256(parsed.payload) !== parsed.header.content_digest) {
    return {
      target,
      path: spec.path,
      status: "hand-edited",
      detail: detailFor("hand-edited", target, spec.path),
      profile: parsed.header.profile,
      ...recorded,
    };
  }

  const stale = {
    target,
    path: spec.path,
    status: "stale",
    detail: detailFor("stale", target, spec.path),
    profile: parsed.header.profile,
    ...recorded,
  };
  if (parsed.header.emit_version !== String(EMIT_VERSION)) return stale;
  // A header naming an unknown target or profile cannot be reproduced by the
  // current generator; re-emitting (which records current names) resolves it.
  if (!EMIT_TARGETS[parsed.header.target]?.v1 || !PROFILES[parsed.header.profile]) {
    return stale;
  }

  const expected = buildArtifact({
    sources,
    target: parsed.header.target,
    profile: parsed.header.profile,
  });
  if (expected.artifact !== content) return stale;

  return {
    target,
    path: spec.path,
    status: "ok",
    detail: null,
    profile: parsed.header.profile,
    ...recorded,
  };
}

/**
 * Drift explanation for one target (read-only; W2 §13 item 15). Reuses the
 * SAME primitives the classifier uses (parseEmitHeader, sha256, buildArtifact)
 * to surface WHY a target landed in its class and WHAT a reconcile would do —
 * never re-classifying and never writing. Driven by an already-computed
 * `result` from classifyTarget so the two can never disagree.
 *
 * Block-level provenance note: the artifact header records only file-level
 * digests (source_digest over included files, content_digest over the whole
 * payload), with no per-block manifest, so wouldWritePaths and reconcileCommand
 * are necessarily file-level (the single artifact path), not block-level.
 */
export function explainTarget({ cwd, target, sources, result }) {
  const spec = EMIT_TARGETS[target];
  const path = resolve(cwd, spec.path);
  const exists = existsSync(path);

  let parsed = null;
  if (exists) {
    parsed = parseEmitHeader(canonicalize(readFileSync(path, "utf8")));
  }
  const headerPresent = parsed !== null;
  const managed = headerPresent;

  // The profile a reconcile would recompile against: the one recorded in a
  // managed header, else the default profile a fresh emit uses.
  const expectedProfile = headerPresent ? parsed.header.profile : "default";

  let payloadDigestMatchesHeader = null;
  let emitVersionMatches = null;
  let sourceDigestMatchesCurrent = null;
  if (headerPresent) {
    payloadDigestMatchesHeader = sha256(parsed.payload) === parsed.header.content_digest;
    emitVersionMatches = parsed.header.emit_version === String(EMIT_VERSION);
    // Recompute the source fingerprint the current sources would produce under
    // the header's recorded profile and compare it to the header's record. A
    // reproducible profile is required to rebuild; otherwise the comparison is
    // undefined (null), mirroring the classifier treating it as stale.
    if (EMIT_TARGETS[parsed.header.target]?.v1 && PROFILES[parsed.header.profile]) {
      const expected = buildArtifact({
        sources,
        target: parsed.header.target,
        profile: parsed.header.profile,
      });
      sourceDigestMatchesCurrent = expected.sourceDigest === parsed.header.source_digest;
    }
  }

  // A STALE artifact is only safe to auto-reconcile when its RECORDED
  // target/profile is still reproducible — judged from the threaded recorded
  // fields, the SAME test `--reconcile` applies (single source of truth). An
  // unreproducible STALE (header records an unknown profile / non-v1 target) is
  // one reconcile REFUSES, so explain must not advertise --reconcile for it.
  const staleReproducible = result.status === "stale" && staleIsReproducible(result);

  // The command a user runs to fix this target, by class:
  //   MISSING / reproducible STALE -> safe to auto-reconcile. `emit --reconcile`
  //     re-emits respecting the recorded profile, so a `full` artifact stays
  //     `full` (it never silently degrades to default).
  //   unreproducible STALE         -> reconcile refuses; re-emit explicitly with
  //     a known profile.
  //   UNMANAGED                    -> needs --force (headerless file).
  //   HAND-EDITED                  -> port the edit into .agentctx/, then emit.
  //   OK                           -> nothing to reconcile; plain emit refreshes.
  const reconcileCommand =
    result.status === "missing" || staleReproducible
      ? `mind-ontology emit --reconcile --target ${target}`
      : result.status === "unmanaged"
        ? `mind-ontology emit --force --target ${target}`
        : `mind-ontology emit --target ${target}`;

  // A reconcile rewrites exactly this target's artifact. OK needs no reconcile,
  // so nothing would be written.
  const wouldWritePaths = result.status === "ok" ? [] : [spec.path];

  return {
    status: result.status,
    managed,
    headerPresent,
    payloadDigestMatchesHeader,
    sourceDigestMatchesCurrent,
    emitVersionMatches,
    expectedProfile,
    reconcileCommand,
    wouldWritePaths,
  };
}

// Human-readable explanation sentence(s) for the text surface (W2 §13 item
// 15): why this class, and what a reconcile would involve. Keyed by status so
// the wording stays in lock-step with the classifier.
export function explainText(result, explain) {
  switch (result.status) {
    case "missing":
      return `why: no artifact on disk (header absent). A reconcile would emit it, writing ${explain.wouldWritePaths.join(", ")}: ${explain.reconcileCommand}`;
    case "unmanaged":
      return `why: file exists but has no emit header, so emit will not claim it. Move the hand-written content into .agentctx/ sources, then reconcile (writes ${explain.wouldWritePaths.join(", ")}): ${explain.reconcileCommand}`;
    case "hand-edited":
      return `why: the payload no longer matches the header's content_digest (edited after generation). Re-emit overwrites the file, so move the hand-edit into the .agentctx/ source first, then re-emit (writes ${explain.wouldWritePaths.join(", ")}): ${explain.reconcileCommand}`;
    case "stale": {
      // An unreproducible STALE cannot be auto-reconciled (reconcile refuses
      // it); explain must say so and point at the manual re-emit, not advertise
      // --reconcile. The reproducible reasons keep the auto-reconcile wording.
      if (!staleIsReproducible(result)) {
        return `why: the recorded target/profile is no longer reproducible, so this CANNOT be auto-reconciled; re-emit it explicitly with a known profile (writes ${explain.wouldWritePaths.join(", ")}): ${explain.reconcileCommand}`;
      }
      const reason = explain.emitVersionMatches === false
        ? "emit_version bumped since last emit"
        : ".agentctx/ sources changed since last emit";
      return `why: ${reason} (profile ${explain.expectedProfile}). A reconcile re-emits it, writing ${explain.wouldWritePaths.join(", ")}: ${explain.reconcileCommand}`;
    }
    default:
      return `why: payload and source digests both match the header under profile ${explain.expectedProfile}; nothing to reconcile.`;
  }
}

