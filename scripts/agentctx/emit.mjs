/**
 * mind-ontology emit — compile static per-tool artifacts from .agentctx/.
 *
 * Implements ADL W3 of the Workbench v1 design packet against the normative
 * specs:
 *   - docs/workbench-w1-emit-target-spec.md (targets, profiles, header,
 *     determinism, drift classes)
 *   - docs/workbench-w2-cli-spec.md (flags, streams, exit codes, JSON shapes)
 *
 * Determinism invariant (W1 §7): emitted bytes are a pure function of
 * (canonicalized included source bytes, target id, profile, emit_version).
 * No timestamps, no machine identity, no locale-dependent ordering, no
 * randomness, no network or model calls anywhere in this module.
 */

