export function parseEmitArgv(argv = process.argv.slice(2)) {
  const parsed = {
    targets: [],
    targetFlagGiven: false,
    check: false,
    explain: false,
    reconcile: false,
    blockManifest: false,
    force: false,
    full: false,
    format: "text",
    cwd: process.cwd(),
    help: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--target") {
      parsed.targetFlagGiven = true;
      const ids = String(argv[++i] ?? "")
        .split(",")
        .map((id) => id.trim())
        .filter(Boolean);
      parsed.targets.push(...ids);
    } else if (arg === "--check") {
      parsed.check = true;
    } else if (arg === "--explain") {
      parsed.explain = true;
    } else if (arg === "--reconcile") {
      parsed.reconcile = true;
    } else if (arg === "--block-manifest") {
      parsed.blockManifest = true;
    } else if (arg === "--force") {
      parsed.force = true;
    } else if (arg === "--full") {
      parsed.full = true;
    } else if (arg === "--format") {
      const f = argv[++i] ?? "";
      if (f !== "text" && f !== "json") {
        throw new Error(`--format must be "text" or "json", got: ${f}`);
      }
      parsed.format = f;
    } else if (arg === "--cwd") {
      parsed.cwd = resolve(argv[++i] ?? parsed.cwd);
    } else if (arg === "-h" || arg === "--help") {
      parsed.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  const allowed = V1_TARGET_IDS.map((t) => `"${t}"`).join(", ");
  for (const id of parsed.targets) {
    if (!EMIT_TARGETS[id]?.v1) {
      throw new Error(`--target must be one of ${allowed}, got: ${id}`);
    }
  }
  if (parsed.targetFlagGiven && parsed.targets.length === 0) {
    throw new Error(`--target must be one of ${allowed}, got: (empty)`);
  }
  if (parsed.full && parsed.check) {
    throw new Error(
      "--full cannot be combined with --check (--check verifies against the profile recorded in the artifact header)",
    );
  }
  if (parsed.force && parsed.check) {
    throw new Error("--force cannot be combined with --check (--check never writes)");
  }
  // --explain is a read-only annotation on the check verdict; it has no meaning
  // in write mode and must never write, so it is valid only alongside --check.
  if (parsed.explain && !parsed.check) {
    throw new Error("--explain is only valid together with --check (it never writes)");
  }
  // --reconcile is a write mode; --check is read-only. They are opposite modes,
  // so combining them is a usage error.
  if (parsed.reconcile && parsed.check) {
    throw new Error("--reconcile cannot be combined with --check (--check never writes; --reconcile is the write side)");
  }
  // --reconcile re-emits each STALE target against the profile recorded in its
  // header, so honoring --full would silently override that recorded profile
  // (e.g. degrade or upgrade a target). Refuse the combination outright.
  if (parsed.reconcile && parsed.full) {
    throw new Error("--full cannot be combined with --reconcile (--reconcile re-emits each target against the profile recorded in its header)");
  }
  // --block-manifest is an opt-in, read-only annotation that attaches per-block
  // provenance to the structured check verdict. It is structured data with no
  // text rendering, and it never writes, so it is valid ONLY alongside
  // --check --explain --format json. Any other combination is a usage error.
  if (
    parsed.blockManifest &&
    !(parsed.check && parsed.explain && parsed.format === "json")
  ) {
    throw new Error(
      "--block-manifest is only valid with --check --explain --format json (it is read-only structured provenance and never writes)",
    );
  }

  // Default target set: all v1 targets (W1 §2, W2 §8 ruling). Explicit
  // targets are deduped and processed in registry order regardless of flag
  // order (determinism, W2 §7.1).
  const requested = parsed.targetFlagGiven ? new Set(parsed.targets) : new Set(V1_TARGET_IDS);
  parsed.targets = V1_TARGET_IDS.filter((id) => requested.has(id));

  return parsed;
}

// W2 §8(b) dual-target advisory — a pure function of the flag set, printed
// only on a no---target emit of both v1 targets; never in artifact bytes.
export const DUAL_TARGET_NOTE = [
  "note: AGENTS.md and CLAUDE.md carry identical payloads. If every tool you run",
  "reads AGENTS.md, emit only it: mind-ontology emit --target agents-md",
].join("\n");

function budgetWarning(build) {
  const contributors = [...build.lineContributions.entries()]
    .filter(([, lines]) => lines > 0)
    .sort(
      (a, b) => b[1] - a[1] || SOURCE_FILES.indexOf(a[0]) - SOURCE_FILES.indexOf(b[0]),
    )
    .slice(0, 3)
    .map(([file, lines]) => `${file} (${lines} lines)`)
    .join(", ");
  return `warning: ${build.target} payload is ${build.payloadLines} lines (soft budget ${PAYLOAD_LINE_BUDGET}); largest contributors: ${contributors}`;
}

/**
 * Write mode (W2 §7.2). Returns { exitCode, stdout, stderr }.
 * Multi-target emits are all-or-nothing: an UNMANAGED refusal writes nothing
 * for any target. Rewriting a fresh artifact is a byte-identical no-op.
 */
export function runEmitWrite(options) {
  validateAgentctxSources(options.cwd);
  const sources = readAgentctx(options.cwd);
  const profile = options.full ? "full" : "default";

  const builds = options.targets.map((target) => buildArtifact({ sources, target, profile }));

  // Refusal scan before any write (W1 §9): an existing headerless file is
  // never silently overwritten and never merged; --force is the only override.
  if (!options.force) {
    for (const build of builds) {
      const path = resolve(options.cwd, build.path);
      if (
        existsSync(path) &&
        parseEmitHeader(canonicalize(readFileSync(path, "utf8"))) === null
      ) {
        throw new Error(
          `Refusing to overwrite ${build.path}: file exists but has no emit header. Move its content into .agentctx/ sources, then re-run with --force to overwrite.`,
        );
      }
    }
  }

  for (const build of builds) {
    const path = resolve(options.cwd, build.path);
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, build.artifact, "utf8");
  }

  const warnings = [];
  for (const build of builds) {
    if (build.payloadLines > PAYLOAD_LINE_BUDGET) {
      warnings.push({
        type: "budget-overflow",
        target: build.target,
        payload_lines: build.payloadLines,
        budget: PAYLOAD_LINE_BUDGET,
        message: budgetWarning(build),
      });
    }
  }
  if (!options.targetFlagGiven) {
    warnings.push({ type: "dual-target-note", message: DUAL_TARGET_NOTE });
  }

  let stdout;
  if (options.format === "json") {
    stdout =
      JSON.stringify(
        {
          ok: true,
          written: builds.map((build) => ({
            target: build.target,
            path: build.path,
            profile: build.profile,
            emit_version: EMIT_VERSION,
            source_digest: build.sourceDigest,
            content_digest: build.contentDigest,
            payload_lines: build.payloadLines,
          })),
          warnings: warnings.map(({ message, ...rest }) => rest),
        },
        null,
        2,
      ) + "\n";
  } else {
    stdout =
      builds
        .map(
          (build) =>
            `WROTE  ${build.path}  (${build.target}, profile ${build.profile}, ${build.payloadLines} payload lines)`,
        )
        .join("\n") + "\n";
  }

  const stderr = warnings.map((w) => w.message).join("\n");
  return { exitCode: 0, stdout, stderr: stderr ? `${stderr}\n` : "" };
}

const TEXT_STATUS = {
  ok: "OK",
  missing: "MISSING",
  unmanaged: "UNMANAGED",
  "hand-edited": "HAND-EDITED",
  stale: "STALE",
};

/**
 * The check verdict as data (W2 §7.4 shape) — one shape, two consumers:
 * `emit --check --format json` prints it verbatim, and the W7 `status`
 * command embeds it verbatim as its emit section.
 *
 * `explainByTarget` (a Map target -> explain object) is opt-in: when omitted
 * the shape is byte-for-byte the frozen base shape (mandatory back-compat, and
 * what `status` always embeds). When present, each target gains an `explain`
 * key. It is never set on the `status` path.
 *
 * `manifestByTarget` (a Map target -> block-manifest array | null) is a further
 * opt-in: when present each target gains a `block_manifest` key carrying the
 * per-block provenance (Phase 2), or null when the recorded profile is not
 * reproducible. Only ever set under --check --explain --format json
 * --block-manifest; never on the `status` path.
 */
export function checkResultJson(results, explainByTarget = null, manifestByTarget = null) {
  return {
    ok: results.every((r) => r.status === "ok"),
    targets: results.map((r) => {
      const base = {
        target: r.target,
        path: r.path,
        status: r.status,
        detail: r.detail,
      };
      if (explainByTarget) base.explain = explainByTarget.get(r.target);
      if (manifestByTarget) base.block_manifest = manifestByTarget.get(r.target);
      return base;
    }),
  };
}

/**
 * The check verdict as text: one line per target plus the frozen summary
 * line ("OK - n of n targets fresh" / "DRIFT - n of m targets need
 * attention"). Shared with `status` so both screens render freshness
 * identically.
 */
export function renderCheckText(results, explainByTarget = null) {
  const drifted = results.filter((r) => r.status !== "ok");
  const lines = [];
  for (const r of results) {
    lines.push(
      r.status === "ok"
        ? `${TEXT_STATUS.ok.padEnd(13)}${r.path} (${r.target}, profile ${r.profile})`
        : `${TEXT_STATUS[r.status].padEnd(13)}${r.path} (${r.target}) - ${r.detail}`,
    );
    // --explain only: an indented explanation line after each classification.
    // Absent explainByTarget (the default and the `status` embed) leaves the
    // existing output byte-for-byte unchanged.
    if (explainByTarget) {
      lines.push(`             ${explainText(r, explainByTarget.get(r.target))}`);
    }
  }
  lines.push(
    drifted.length === 0
      ? `OK - ${results.length} of ${results.length} targets fresh`
      : `DRIFT - ${drifted.length} of ${results.length} targets need attention`,
  );
  return lines.join("\n") + "\n";
}

/**
 * Classify every requested target from in-memory sources (the `--check`
 * core, also embedded by `status`).
 */
export function checkTargets({ cwd, targets, sources }) {
  return targets.map((target) => classifyTarget({ cwd, target, sources }));
}

/**
 * Check mode (W1 §8, W2 §7.3-7.4). Writes nothing. A negative verdict is a
 * successful report of a bad state: report on stdout, exit 1. Hard errors
 * (compile failure, usage errors) are thrown and exit 2 in the entry point.
 */
export function runEmitCheck(options) {
  validateAgentctxSources(options.cwd);
  const sources = readAgentctx(options.cwd);

  const results = checkTargets({ cwd: options.cwd, targets: options.targets, sources });
  const ok = results.every((r) => r.status === "ok");

  // --explain (read-only): derive the per-target explanation from the same
  // sources and the already-computed classification. Off by default, so the
  // base text/json output stays byte-for-byte unchanged.
  const explainByTarget = options.explain
    ? new Map(
        results.map((r) => [
          r.target,
          explainTarget({ cwd: options.cwd, target: r.target, sources, result: r }),
        ]),
      )
    : null;

  // --block-manifest (read-only, opt-in; requires --explain --format json):
  // attach the per-block provenance the current sources produce for each target
  // under the profile --explain already reports (recorded profile if managed,
  // else default). It is null whenever the recorded header is NOT reproducible
  // — an unknown recorded profile OR a non-v1 recorded target — so it stays in
  // lock-step with --explain's own reproducibility signal
  // (sourceDigestMatchesCurrent === null while a header is present) and never
  // claims provenance for an artifact it cannot rebuild. Reuses buildArtifact;
  // no writes.
  const manifestByTarget = options.blockManifest
    ? new Map(
        results.map((r) => {
          const explain = explainByTarget.get(r.target);
          // A header is present but its recorded target/profile cannot be
          // reproduced (the same gate classify/reconcile/explain apply): the
          // on-disk artifact's provenance is unrecomputable -> null.
          if (explain.headerPresent && explain.sourceDigestMatchesCurrent === null) {
            return [r.target, null];
          }
          // Otherwise expectedProfile is reproducible: the recorded profile for
          // a managed target, or the default for a fresh (MISSING/UNMANAGED)
          // emit. PROFILES guard is defensive — this branch only sees known
          // profiles.
          const manifest = PROFILES[explain.expectedProfile]
            ? buildArtifact({ sources, target: r.target, profile: explain.expectedProfile }).blockManifest
            : null;
          return [r.target, manifest];
        }),
      )
    : null;

  const stdout =
    options.format === "json"
      ? JSON.stringify(checkResultJson(results, explainByTarget, manifestByTarget), null, 2) + "\n"
      : renderCheckText(results, explainByTarget);

  return { exitCode: ok ? 0 : 1, stdout, stderr: "" };
}

// A reconcile refuses any class where a re-emit could destroy unrecoverable
// work or cannot be reproduced safely. The message names the manual fix.
function reconcileRefusal(result) {
  switch (result.status) {
    case "unmanaged":
      return `${result.path} (${result.target}) is UNMANAGED (a headerless file emit will not claim); reconcile will not overwrite it. Move its content into .agentctx/ sources, then run: mind-ontology emit --force --target ${result.target}`;
    case "hand-edited":
      return `${result.path} (${result.target}) is HAND-EDITED; a re-emit would discard the hand edit. Move the edit into the .agentctx/ source, then run: mind-ontology emit --target ${result.target}`;
    default:
      // A STALE target whose recorded target/profile is no longer reproducible
      // (unknown profile / non-v1 target in the header): re-emitting would have
      // to invent a profile, which could silently change the artifact's scope.
      return `${result.path} (${result.target}) records a target/profile that is no longer reproducible; reconcile will not guess a profile. Re-emit it explicitly with a known profile.`;
  }
}

// Whether a STALE artifact can be reconciled by rebuilding against the
// target/profile RECORDED IN ITS HEADER. emit_version-only staleness is
// reproducible (the recorded names are still known); a header recording an
// unknown profile or a non-v1 target is NOT — judged from the recorded fields,
// never the requested target (the same test classifyTarget itself applies, and
// what --check --explain reports). A missing/headerless result has no recorded
// fields and is handled by other branches, never reaches here.
function staleIsReproducible(result) {
  return Boolean(
    EMIT_TARGETS[result.recordedTarget]?.v1 && PROFILES[result.recordedProfile],
  );
}

/**
 * Reconcile mode (Lane 2). SAFE drift repair: writes generated artifacts
 * (AGENTS.md / CLAUDE.md) only — never `.agentctx/` sources — and only for
 * classes where nothing is lost:
 *   OK          -> skip (already fresh).
 *   MISSING     -> emit at the default profile (nothing on disk to honor).
 *   STALE       -> re-emit against the profile RECORDED IN THE HEADER, so a
 *                  `full` artifact stays `full` (never degraded to default).
 *   UNMANAGED   -> refuse (headerless file; needs --force).
 *   HAND-EDITED -> refuse (a re-emit would discard the hand edit).
 * All-or-nothing: if ANY requested target is in a refuse class, nothing is
 * written for any target. Classification happens first, every write second.
 * Returns { exitCode, stdout, stderr }: 0 reconciled/clean, 1 refused.
 */
export function runEmitReconcile(options) {
  validateAgentctxSources(options.cwd);
  const sources = readAgentctx(options.cwd);

  const results = checkTargets({ cwd: options.cwd, targets: options.targets, sources });

  // Phase 1 — classify and screen. A refuse class anywhere blocks the whole
  // run; collect every reason so the operator sees all of them at once.
  const refusals = results
    .filter(
      (r) =>
        r.status === "unmanaged" ||
        r.status === "hand-edited" ||
        (r.status === "stale" && !staleIsReproducible(r)),
    )
    .map((r) => reconcileRefusal(r));
  if (refusals.length > 0) {
    return {
      exitCode: 1,
      stdout: "",
      stderr:
        `Refusing to reconcile: ${refusals.length} target(s) need a human (nothing written for any target).\n` +
        refusals.map((m) => `  ${m}`).join("\n") +
        "\n",
    };
  }

  // Phase 2 — build every artifact in memory. STALE rebuilds against the
  // target/profile RECORDED IN THE HEADER (the same fields the reproducibility
  // gate and classifyTarget use), so a `full` artifact stays `full`. MISSING
  // has no header, so it emits the requested target at the default profile.
  // No write has happened yet.
  const planned = [];
  for (const r of results) {
    if (r.status === "ok") continue;
    const build =
      r.status === "stale"
        ? buildArtifact({ sources, target: r.recordedTarget, profile: r.recordedProfile })
        : buildArtifact({ sources, target: r.target, profile: "default" });
    planned.push({ result: r, build });
  }

  // Phase 3 — write all planned artifacts. Reconcile only ever writes the v1
  // artifact paths (AGENTS.md / CLAUDE.md), never `.agentctx/`.
  for (const { build } of planned) {
    const path = resolve(options.cwd, build.path);
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, build.artifact, "utf8");
  }

  const reconciled = new Set(planned.map((p) => p.result.target));
  if (options.format === "json") {
    const stdout =
      JSON.stringify(
        {
          ok: true,
          targets: results.map((r) => ({
            target: r.target,
            path: r.path,
            action: reconciled.has(r.target) ? "reconciled" : "skipped",
            status: r.status,
            profile: reconciled.has(r.target)
              ? planned.find((p) => p.result.target === r.target).build.profile
              : (r.profile ?? null),
          })),
        },
        null,
        2,
      ) + "\n";
    return { exitCode: 0, stdout, stderr: "" };
  }

  const lines = results.map((r) => {
    if (!reconciled.has(r.target)) {
      return `SKIP        ${r.path} (${r.target}, already ${TEXT_STATUS[r.status]})`;
    }
    const build = planned.find((p) => p.result.target === r.target).build;
    return `RECONCILED  ${r.path} (${r.target}, was ${TEXT_STATUS[r.status]}, profile ${build.profile})`;
  });
  lines.push(
    reconciled.size === 0
      ? `OK - ${results.length} of ${results.length} targets already fresh`
      : `RECONCILED - ${reconciled.size} of ${results.length} targets re-emitted`,
  );
  return { exitCode: 0, stdout: lines.join("\n") + "\n", stderr: "" };
}

function printHelp() {
  return `mind-ontology emit — compile static per-tool artifacts from .agentctx/

Usage:
  mind-ontology emit [options]             write all v1 targets (AGENTS.md, CLAUDE.md)
  mind-ontology emit --check [options]     verify freshness; writes nothing
  mind-ontology emit --reconcile [options] safely re-emit only drifted targets

Options:
  --target <id>[,<id>]   Restrict to targets: agents-md, claude-md. Repeatable.
  --check                Classify each target (OK/MISSING/UNMANAGED/HAND-EDITED/STALE).
                         Exit 0 fresh, 1 drift, 2 error.
  --explain              With --check only: explain why each target got its class
                         and what a reconcile would write. Read-only; never writes.
  --block-manifest       With --check --explain --format json only: attach
                         per-block provenance (source_file/source_block_index/
                         source_block_digest/rendered_digest/...) to each target.
                         Read-only; never writes.
  --reconcile            Repair drift safely: re-emit MISSING/STALE targets
                         (STALE keeps its recorded profile), SKIP OK ones, and
                         REFUSE UNMANAGED/HAND-EDITED (writing nothing for any
                         target). Writes artifacts only, never .agentctx/.
                         Exit 0 reconciled/clean, 1 refused, 2 error.
  --force                Overwrite an existing un-managed file (one without an emit
                         header). Never needed for refreshing managed artifacts.
  --full                 Emit the whole ontology instead of the default profile.
  --format text|json     Output format (default: text).
  --cwd <path>           Project root containing .agentctx/ (default: cwd).
  -h, --help             Show this help message.

Both files carry identical payloads by design; if every tool you run reads
AGENTS.md, emit only it: mind-ontology emit --target agents-md
`;
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);

if (isMain) {
  // Exit-code contract (W2 §2.4): write mode is 0/1; check mode is the
  // three-way split — 0 fresh, 1 drift, 2 hard error (usage errors and
  // compile failures included, so `1` means exactly "re-emit needed").
  const argv = process.argv.slice(2);
  // Exit code on a thrown usage/parse error. --check and --explain imply
  // check-mode intent; --reconcile is a distinct mode that also reserves exit 2
  // for usage errors (1 stays reserved for a reconcile refusal — a real verdict,
  // not a usage mistake). Plain write-mode usage errors stay exit 1.
  const usageExit2 =
    argv.includes("--check") ||
    argv.includes("--explain") ||
    argv.includes("--reconcile") ||
    argv.includes("--block-manifest");
  try {
    const options = parseEmitArgv(argv);
    if (options.help) {
      process.stdout.write(printHelp());
      process.exit(0);
    }
    const result = options.check
      ? runEmitCheck(options)
      : options.reconcile
        ? runEmitReconcile(options)
        : runEmitWrite(options);
    if (result.stdout) process.stdout.write(result.stdout);
    if (result.stderr) process.stderr.write(result.stderr);
    process.exit(result.exitCode);
  } catch (error) {
    process.stderr.write(`${error.message}\n`);
    process.exit(usageExit2 ? 2 : 1);
  }
}
