export function parseEmitArgv(argv = process.argv.slice(2)) {
  const parsed = {
    targets: [],
    targetFlagGiven: false,
    check: false,
    explain: false,
    force: false,
    full: false,
    format: "text",
    cwd: process.cwd(),
    help: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--target") {
      parsed.targetFlagGiven = true;
      const ids = String(argv[++i] ?? "")
        .split(",")
        .map((id) => id.trim())
        .filter(Boolean);
      parsed.targets.push(...ids);
    } else if (arg === "--check") {
      parsed.check = true;
    } else if (arg === "--explain") {
      parsed.explain = true;
    } else if (arg === "--force") {
      parsed.force = true;
    } else if (arg === "--full") {
      parsed.full = true;
    } else if (arg === "--format") {
      const f = argv[++i] ?? "";
      if (f !== "text" && f !== "json") {
        throw new Error(`--format must be "text" or "json", got: ${f}`);
      }
      parsed.format = f;
    } else if (arg === "--cwd") {
      parsed.cwd = resolve(argv[++i] ?? parsed.cwd);
    } else if (arg === "-h" || arg === "--help") {
      parsed.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  const allowed = V1_TARGET_IDS.map((t) => `"${t}"`).join(", ");
  for (const id of parsed.targets) {
    if (!EMIT_TARGETS[id]?.v1) {
      throw new Error(`--target must be one of ${allowed}, got: ${id}`);
    }
  }
  if (parsed.targetFlagGiven && parsed.targets.length === 0) {
    throw new Error(`--target must be one of ${allowed}, got: (empty)`);
  }
  if (parsed.full && parsed.check) {
    throw new Error(
      "--full cannot be combined with --check (--check verifies against the profile recorded in the artifact header)",
    );
  }
  if (parsed.force && parsed.check) {
    throw new Error("--force cannot be combined with --check (--check never writes)");
  }
  // --explain is a read-only annotation on the check verdict; it has no meaning
  // in write mode and must never write, so it is valid only alongside --check.
  if (parsed.explain && !parsed.check) {
    throw new Error("--explain is only valid together with --check (it never writes)");
  }

  // Default target set: all v1 targets (W1 §2, W2 §8 ruling). Explicit
  // targets are deduped and processed in registry order regardless of flag
  // order (determinism, W2 §7.1).
  const requested = parsed.targetFlagGiven ? new Set(parsed.targets) : new Set(V1_TARGET_IDS);
  parsed.targets = V1_TARGET_IDS.filter((id) => requested.has(id));

  return parsed;
}

// W2 §8(b) dual-target advisory — a pure function of the flag set, printed
// only on a no---target emit of both v1 targets; never in artifact bytes.
export const DUAL_TARGET_NOTE = [
  "note: AGENTS.md and CLAUDE.md carry identical payloads. If every tool you run",
  "reads AGENTS.md, emit only it: mind-ontology emit --target agents-md",
].join("\n");

function budgetWarning(build) {
  const contributors = [...build.lineContributions.entries()]
    .filter(([, lines]) => lines > 0)
    .sort(
      (a, b) => b[1] - a[1] || SOURCE_FILES.indexOf(a[0]) - SOURCE_FILES.indexOf(b[0]),
    )
    .slice(0, 3)
    .map(([file, lines]) => `${file} (${lines} lines)`)
    .join(", ");
  return `warning: ${build.target} payload is ${build.payloadLines} lines (soft budget ${PAYLOAD_LINE_BUDGET}); largest contributors: ${contributors}`;
}

/**
 * Write mode (W2 §7.2). Returns { exitCode, stdout, stderr }.
 * Multi-target emits are all-or-nothing: an UNMANAGED refusal writes nothing
 * for any target. Rewriting a fresh artifact is a byte-identical no-op.
 */
export function runEmitWrite(options) {
  validateAgentctxSources(options.cwd);
  const sources = readAgentctx(options.cwd);
  const profile = options.full ? "full" : "default";

  const builds = options.targets.map((target) => buildArtifact({ sources, target, profile }));

  // Refusal scan before any write (W1 §9): an existing headerless file is
  // never silently overwritten and never merged; --force is the only override.
  if (!options.force) {
    for (const build of builds) {
      const path = resolve(options.cwd, build.path);
      if (
        existsSync(path) &&
        parseEmitHeader(canonicalize(readFileSync(path, "utf8"))) === null
      ) {
        throw new Error(
          `Refusing to overwrite ${build.path}: file exists but has no emit header. Move its content into .agentctx/ sources, then re-run with --force to overwrite.`,
        );
      }
    }
  }

  for (const build of builds) {
    const path = resolve(options.cwd, build.path);
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, build.artifact, "utf8");
  }

  const warnings = [];
  for (const build of builds) {
    if (build.payloadLines > PAYLOAD_LINE_BUDGET) {
      warnings.push({
        type: "budget-overflow",
        target: build.target,
        payload_lines: build.payloadLines,
        budget: PAYLOAD_LINE_BUDGET,
        message: budgetWarning(build),
      });
    }
  }
  if (!options.targetFlagGiven) {
    warnings.push({ type: "dual-target-note", message: DUAL_TARGET_NOTE });
  }

  let stdout;
  if (options.format === "json") {
    stdout =
      JSON.stringify(
        {
          ok: true,
          written: builds.map((build) => ({
            target: build.target,
            path: build.path,
            profile: build.profile,
            emit_version: EMIT_VERSION,
            source_digest: build.sourceDigest,
            content_digest: build.contentDigest,
            payload_lines: build.payloadLines,
          })),
          warnings: warnings.map(({ message, ...rest }) => rest),
        },
        null,
        2,
      ) + "\n";
  } else {
    stdout =
      builds
        .map(
          (build) =>
            `WROTE  ${build.path}  (${build.target}, profile ${build.profile}, ${build.payloadLines} payload lines)`,
        )
        .join("\n") + "\n";
  }

  const stderr = warnings.map((w) => w.message).join("\n");
  return { exitCode: 0, stdout, stderr: stderr ? `${stderr}\n` : "" };
}

const TEXT_STATUS = {
  ok: "OK",
  missing: "MISSING",
  unmanaged: "UNMANAGED",
  "hand-edited": "HAND-EDITED",
  stale: "STALE",
};

/**
 * The check verdict as data (W2 §7.4 shape) — one shape, two consumers:
 * `emit --check --format json` prints it verbatim, and the W7 `status`
 * command embeds it verbatim as its emit section.
 *
 * `explainByTarget` (a Map target -> explain object) is opt-in: when omitted
 * the shape is byte-for-byte the frozen base shape (mandatory back-compat, and
 * what `status` always embeds). When present, each target gains an `explain`
 * key. It is never set on the `status` path.
 */
export function checkResultJson(results, explainByTarget = null) {
  return {
    ok: results.every((r) => r.status === "ok"),
    targets: results.map((r) => {
      const base = {
        target: r.target,
        path: r.path,
        status: r.status,
        detail: r.detail,
      };
      if (explainByTarget) base.explain = explainByTarget.get(r.target);
      return base;
    }),
  };
}

/**
 * The check verdict as text: one line per target plus the frozen summary
 * line ("OK - n of n targets fresh" / "DRIFT - n of m targets need
 * attention"). Shared with `status` so both screens render freshness
 * identically.
 */
export function renderCheckText(results, explainByTarget = null) {
  const drifted = results.filter((r) => r.status !== "ok");
  const lines = [];
  for (const r of results) {
    lines.push(
      r.status === "ok"
        ? `${TEXT_STATUS.ok.padEnd(13)}${r.path} (${r.target}, profile ${r.profile})`
        : `${TEXT_STATUS[r.status].padEnd(13)}${r.path} (${r.target}) - ${r.detail}`,
    );
    // --explain only: an indented explanation line after each classification.
    // Absent explainByTarget (the default and the `status` embed) leaves the
    // existing output byte-for-byte unchanged.
    if (explainByTarget) {
      lines.push(`             ${explainText(r, explainByTarget.get(r.target))}`);
    }
  }
  lines.push(
    drifted.length === 0
      ? `OK - ${results.length} of ${results.length} targets fresh`
      : `DRIFT - ${drifted.length} of ${results.length} targets need attention`,
  );
  return lines.join("\n") + "\n";
}

/**
 * Classify every requested target from in-memory sources (the `--check`
 * core, also embedded by `status`).
 */
export function checkTargets({ cwd, targets, sources }) {
  return targets.map((target) => classifyTarget({ cwd, target, sources }));
}

/**
 * Check mode (W1 §8, W2 §7.3-7.4). Writes nothing. A negative verdict is a
 * successful report of a bad state: report on stdout, exit 1. Hard errors
 * (compile failure, usage errors) are thrown and exit 2 in the entry point.
 */
export function runEmitCheck(options) {
  validateAgentctxSources(options.cwd);
  const sources = readAgentctx(options.cwd);

  const results = checkTargets({ cwd: options.cwd, targets: options.targets, sources });
  const ok = results.every((r) => r.status === "ok");

  // --explain (read-only): derive the per-target explanation from the same
  // sources and the already-computed classification. Off by default, so the
  // base text/json output stays byte-for-byte unchanged.
  const explainByTarget = options.explain
    ? new Map(
        results.map((r) => [
          r.target,
          explainTarget({ cwd: options.cwd, target: r.target, sources, result: r }),
        ]),
      )
    : null;

  const stdout =
    options.format === "json"
      ? JSON.stringify(checkResultJson(results, explainByTarget), null, 2) + "\n"
      : renderCheckText(results, explainByTarget);

  return { exitCode: ok ? 0 : 1, stdout, stderr: "" };
}

function printHelp() {
  return `mind-ontology emit — compile static per-tool artifacts from .agentctx/

Usage:
  mind-ontology emit [options]            write all v1 targets (AGENTS.md, CLAUDE.md)
  mind-ontology emit --check [options]    verify freshness; writes nothing

Options:
  --target <id>[,<id>]   Restrict to targets: agents-md, claude-md. Repeatable.
  --check                Classify each target (OK/MISSING/UNMANAGED/HAND-EDITED/STALE).
                         Exit 0 fresh, 1 drift, 2 error.
  --explain              With --check only: explain why each target got its class
                         and what a reconcile would write. Read-only; never writes.
  --force                Overwrite an existing un-managed file (one without an emit
                         header). Never needed for refreshing managed artifacts.
  --full                 Emit the whole ontology instead of the default profile.
  --format text|json     Output format (default: text).
  --cwd <path>           Project root containing .agentctx/ (default: cwd).
  -h, --help             Show this help message.

Both files carry identical payloads by design; if every tool you run reads
AGENTS.md, emit only it: mind-ontology emit --target agents-md
`;
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);

if (isMain) {
  // Exit-code contract (W2 §2.4): write mode is 0/1; check mode is the
  // three-way split — 0 fresh, 1 drift, 2 hard error (usage errors and
  // compile failures included, so `1` means exactly "re-emit needed").
  const argv = process.argv.slice(2);
  // --explain implies check-mode intent (it is only valid with --check), so a
  // usage error mentioning either gets the check-mode exit 2, keeping 1 reserved
  // for "re-emit needed".
  const checkMode = argv.includes("--check") || argv.includes("--explain");
  try {
    const options = parseEmitArgv(argv);
    if (options.help) {
      process.stdout.write(printHelp());
      process.exit(0);
    }
    const result = options.check ? runEmitCheck(options) : runEmitWrite(options);
    if (result.stdout) process.stdout.write(result.stdout);
    if (result.stderr) process.stderr.write(result.stderr);
    process.exit(result.exitCode);
  } catch (error) {
    process.stderr.write(`${error.message}\n`);
    process.exit(checkMode ? 2 : 1);
  }
}
